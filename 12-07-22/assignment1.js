function add(num1, num2){
    return num1+num2;
}
function sub(num1, num2){
    return num1-num2;
}
function div(num1, num2){
    return num1/num2;
}
function mul(num1, num2){
    return num1*num2; 
}
console.log(add(12,8));
console.log(sub(23,10));
console.log(div(8, 5));
console.log(mul(20,-5)); 